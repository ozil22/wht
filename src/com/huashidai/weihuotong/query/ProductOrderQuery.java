package com.huashidai.weihuotong.query;

public class ProductOrderQuery extends BaseQuery {
	/**
	 * 订单状态
	 */
	private Integer state = -99;
	/**
	 * 店铺名字
	 */
	private String storeName;
	/**
	 * 用户名字
	 */
	private String userName;
	/**
	 * 店铺id
	 */
	private Long storeId = -1l;
	/**
	 * 用户id
	 */
	private Long userId = -1l;
	/**
	 * 客户id
	 */
	private Long customerId = -1l;
	/**
	 * 购买代理商品客户名字
	 */
	private String customerName;
	/**
	 * 代理名字
	 */
	private String proxyUserName;
	/**
	 * 订单号
	 */
	private String orderNum;
	/**
	 * 订单类型0销售订单,1代理订单
	 */
	private Integer orderType;

	public Integer getState() {
		return state;
	}

	public void setState(Integer state) {
		this.state = state;
	}

	public String getStoreName() {
		return storeName;
	}

	public void setStoreName(String storeName) {
		this.storeName = storeName;
	}

	public String getUserName() {
		return userName;
	}

	public void setUserName(String userName) {
		this.userName = userName;
	}

	public String getOrderNum() {
		return orderNum;
	}

	public void setOrderNum(String orderNum) {
		this.orderNum = orderNum;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getProxyUserName() {
		return proxyUserName;
	}

	public void setProxyUserName(String proxyUserName) {
		this.proxyUserName = proxyUserName;
	}

	public Integer getOrderType() {
		return orderType;
	}

	public void setOrderType(Integer orderType) {
		this.orderType = orderType;
	}

	public Long getStoreId() {
		return storeId;
	}

	public void setStoreId(Long storeId) {
		this.storeId = storeId;
	}

	public Long getUserId() {
		return userId;
	}

	public void setUserId(Long userId) {
		this.userId = userId;
	}
	

	public Long getCustomerId() {
		return customerId;
	}

	public void setCustomerId(Long customerId) {
		this.customerId = customerId;
	}

	@Override
	public String toString() {
		return super.toString() + state + "," + storeName + ","
				+ userName + "," + storeId + "," + userId + "," + customerId
				+ "," + customerName + "," + proxyUserName + "," + orderNum
				+ "," + orderType;
	}

	
}
