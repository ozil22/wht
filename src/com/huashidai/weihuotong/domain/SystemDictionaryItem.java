package com.huashidai.weihuotong.domain;
/**
 * 数据字典明细
 * @author ozil
 */
public class SystemDictionaryItem {
	private Long id;
	private Integer sn;//数据字典明细序号
	private String name;//数据字典明细名
	private String intro;//字典目录明细简介
	
	//字典目录     多对一
	private SystemDictionary parent;

	@Override
	public String toString() {
		return "SystemDictionaryItem [id=" + id + ", sn=" + sn + ", name="
				+ name + ", intro=" + intro + ", parent=" + parent + "]";
	}

	public Long getId() {
		return id;
	}

	public void setId(Long id) {
		this.id = id;
	}

	public Integer getSn() {
		return sn;
	}

	public void setSn(Integer sn) {
		this.sn = sn;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIntro() {
		return intro;
	}

	public void setIntro(String intro) {
		this.intro = intro;
	}

	public SystemDictionary getParent() {
		return parent;
	}

	public void setParent(SystemDictionary parent) {
		this.parent = parent;
	}
	
	
}

